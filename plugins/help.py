from telethon import events

# التعديل هنا: جعلناه يرد على (اوامري، الاوامر، اوامر) في سطر واحد
@events.register(events.NewMessage(pattern=r"\.(اوامري|الاوامر|اوامر)$"))
async def help_menu(event):
    # التأكد أنك أنت من أرسل الأمر (عشان ما يشتغل عند غيرك)
    if not event.out:
        return
    
    help_text = """
🛡️ **سورس المتمرد التقني - القائمة المتطورة** 🛡️
─── • ⚡️ • ───

🚀 **[ الفحص والسرعة ]**
• `.بينج` : قياس سرعة استجابة السورس.
• `.وقت` : عرض الوقت والتاريخ (اليمن).

💀 **[ أوامر الهكر والتجسس ]**
• `.كشف` : كشف ايدي وبيو الشخص.
• `.سجل` : كشف سجل الأسماء القديمة.
• `.سحب` : سحب ميديا مقيدة.

🎮 **[ التسلية والترفيه ]**
• `.نكته` | `.صراحة` | `.لو خيروك`

─── • ⚡️ • ───
👤 **المطور:** @Vi_ti0
"""
    await event.edit(help_text)
