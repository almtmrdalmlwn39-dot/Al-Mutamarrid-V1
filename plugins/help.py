from telethon import events

@events.register(events.NewMessage(pattern=r"\.اوامري"))
async def help_menu(event):
    if not event.out:
        return
    
    help_text = """
🛡️ **سورس المتمرد التقني - الإصدار الأقوى** 🛡️
─── • ⚡️ • ───

🚀 **[ الفحص والسرعة ]**
• `.بينج` : قياس سرعة استجابة السورس.
• `.وقت` : عرض الوقت والتاريخ (اليمن).
• `.فحص` : للتأكد من اتصال السورس.

💀 **[ أوامر الهكر والتجسس ]**
• `.ايدي` : كشف ايدي وحساب الشخص.
• `.معلومات` : كشف معلومات الحساب المخفية.
• `.سحب` : اختراق حماية القنوات المقيدة.
• `.كشف` : لمعرفة آخر ظهور (إذا كان متاحاً).

🛡️ **[ نظام الحماية ]**
• `.حماية` : تفعيل/إيقاف حماية الخاص.
• `.سماح` : السماح لشخص بمراسلتك.
• `.رفض` : حظر الشخص وتفعيل الحماية.

🎮 **[ أوامر التسلية والترفيه ]**
• `.نكته` : نكت عشوائية.
• `.لو خيروك` : لعبة الاختيارات.
• `.صراحة` : أسئلة جريئة.

─── • ⚡️ • ───
👤 **المطور:** @Vi_ti0
🌐 **القناة:** @bedmoddinnow
"""
    await event.edit(help_text)
